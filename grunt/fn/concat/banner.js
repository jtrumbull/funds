/*
 |------------------------------------------------------------------------------
 | Grunt\Fn\Concat: Banner
 |------------------------------------------------------------------------------
 */

module.exports = function(grunt) {
  
  'use strict';
  
  var _titize = require('../inflector/titleize.js')(grunt);
  
  var banner = function (title) {
    
    var out = [
      '/*',
      ' |----------------------------------------------------------------------------',
      ' | App: ' + _titize(title),
      ' |----------------------------------------------------------------------------',
      ' */\n'
    ];
    
    return out.join('\n');
    
  }
  
  return banner;
  
};
